package sets;

import java.util.LinkedHashSet;

public class Linked_HashSet {

	public static void main(String[] args) {
		

		LinkedHashSet<Character> set1 = new LinkedHashSet<Character>();
		
		set1.add('a');
		set1.add('e');
		set1.add('i');
		set1.add('o');
		set1.add('u');
		
		System.out.println(set1);
	}

}
